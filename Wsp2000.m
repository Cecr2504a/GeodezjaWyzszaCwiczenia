function Wsp2000 = Wsp2000(F,La)
    a=6378137;
    b=6356752.298;
    e2a=(((a^2)-(b^2))/(a^2));
    e2b=(((a^2)-(b^2))/(b^2));
    t=tan(F);
    mi2=(e2b)*((cos(F))^2);
    L0=19*pi/180;
    A0=1-(e2a/4)-((3*(e2a)^2)/64)-((5*(e2a)^3)/256);
    A2=(3/8)*((e2a+((e2a^2)/4)+((15*(e2a)^3))/128));
    A4=(15/256)*(e2a^2+((3*((e2a)^3))/4));
    A6=(35*((e2a)^3))/3072;
    sigma=a*(A0*F-A2*sin(2*F)+A4*sin(4*F)-A6*sin(6*F));
    l=La-L0;
    N=a/(sqrt(1-e2a*(sin(F))^2));
    Xgk=sigma+((l^2)/2)*N*sin(F)*cos(F)*(1+((l^2)/12)*((cos(F))^2)*(5-(t^2)+9*(mi2)+4*((mi2)^2))+((l^4)/360)*((cos(F))^4)*(61-58*(t^2)+(t^4)+270*(mi2)-330*(mi2)*(t^2)))
    Ygk=l*N*cos(F)*(1+((l^2)/6)*((cos(F))^2)*(1-(t^2)+(mi2))+((l^4)/120)*((cos(F))^4)*(5-18*(t^2)+(t^4)+14*(mi2)-58*(mi2)*(t^2)))
    m02000=0.999923;
    nr=fix(La*180/pi)/3;
    X2000=m02000*Xgk;
    Y2000=m02000*Ygk+1000000*nr+500000;
    Wsp2000=[X2000,Y2000]
    assignin('base','Wsp2000',Wsp2000)
end